package HW6;

public class GameEntity {

    // АТРИБУТЫ

    private int health;
    private int damage;

    // МЕТОДЫ ДОСТУПА

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

}
